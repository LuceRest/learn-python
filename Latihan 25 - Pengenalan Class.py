class mahasiswa():                  # mahasiswa() sebagai class.
    nama = 'nama'                   # nama sebagai attribute.
    pass

otong = mahasiswa()                 # otong sebagai instance.
ucup = mahasiswa()                  # ucup sebagai instance.

otong.nama = "Otong Surotong"       # <instance>.<attribute> -> Berfungsi untuk memanggil variabel isi dari class.
ucup.nama = "Michael Ucup"          # <instance>.<attribute> -> Berfungsi untuk memanggil variabel isi dari class.

print(otong.nama)
print(ucup.nama)


